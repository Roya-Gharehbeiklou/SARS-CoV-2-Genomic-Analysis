# Data Types

# 1- character 
dna_sequence <- as.character()

dna_sequence <- "AAAAAAAAAAAA"


# 2- logical: TRUE or FALSE
is_it_dna <- as.logical()
is_it_dna <- TRUE     # you can use: FALSE 


# 3- integer: 
an_integer <- as.integer()
an_integer <- 12L
an_integer <- 2001234000L
an_integer <- 2201234000L # This code will generate a warning!
                          # In R version 3.x it will crash.


# 4- numeric: 
my_number <- as.numeric()
my_number <- 2e-308
my_number <- 1.03


?character
?logical
?integer
?numeric
